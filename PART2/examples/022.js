// 동등 연산자 '=='
console.log(5 == 5);
console.log("5" == 5);
console.log(5 == '5');

console.log('\n');

// 부등 연산자 '!='
console.log(5 != 1);
console.log(5 != "1");
console.log(5 != "5");

console.log('\n');

// 일치 연산자 '==='
console.log(5 === 5);
console.log(5 === "5");

console.log('\n');

// 불일치 연산자 '!=='
console.log(5 !== 10);
console.log(5 !== "5");