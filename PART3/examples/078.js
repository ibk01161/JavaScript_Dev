// 문자열 대소문자 변환하기 (toLowerCase, toUpperCase)
console.log('Find Your Own Voice'.toLowerCase());                           // 해당 문자열을 모두 소문자로 변환
console.log('Find Your Own Voice'.toUpperCase());                           // 해당 문자열을 모두 대문자로 변환

const value = 'Find Your Own Voice';
console.log(value.toLowerCase() === value.toUpperCase());